class Trig {
    public static void main (String[] args){
        double sine = Math.sin(0.5236);
        double cosine = Math.cos(0.5236);
        double sos = (sine * sine) + (cosine * cosine);
        System.out.println("sine: " + sine + " cosine: " + cosine + " sum of squares: " + sos );

    }
}